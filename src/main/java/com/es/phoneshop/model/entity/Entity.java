package com.es.phoneshop.model.entity;

public abstract class Entity {
    protected Long id;
    protected String code;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
